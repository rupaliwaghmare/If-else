percentage=int(input("enter the number"))
if percentage<40:
    print("F")
elif percentage<=40 or percentage<=60:
    print("E")
elif percentage<=60 or percentage<=70:
    print("D")
elif percentage<=70 or percentage<=80:
    print("C")
elif percentage<=80 or percentage<=90:
    print("B") 
elif percentage>=90:
    print("A")
else:
    ("failing grade")                     